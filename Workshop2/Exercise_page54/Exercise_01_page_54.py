x = 8
y = 2

# a
print("a.   x + y * 3 =", x + y * 3)

# b
print("b.   (x + y) * 3 =", (x + y) * 3)

# c
print("c.   x ^ y =", x ** y)

# d
print("d.   x % y =", x % y)

# e
print("e.   x / 12.0 =", x / 12.0)

# f
print("f.   x / 6 =", x // 6)
